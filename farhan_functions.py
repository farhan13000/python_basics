def rectangle_Area(h, b) -> float:
    """
    Calculate the area of a rectangle given its height and base.

    Parameters:
    h (float): The height of the rectangle.
    b (float): The base of the rectangle.

    Returns:
    float: The area of the rectangle.
    """
    return h * b

def triangle_Area(h, b) -> float:
    """
    Calculate the area of a triangle given its height and base.

    Parameters:
    h (float): The height of the triangle.
    b (float): The base of the triangle.

    Returns:
    float: The area of the triangle.
    """
    return 0.5 * h * b
